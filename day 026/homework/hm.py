# გაქვს სია: `grades = [45, 52, 60, 75, 82, 90, 95, 100]`.
# * Slicing-ის (ჭრის) გამოყენებით ამოიღე სიიდან ბოლო 3 საუკეთესო ქულა.
# * შეამოწმე `if`-ით, არის თუ არა ამ სამი ქულის საშუალო 90-ზე მეტი.


grades = [45, 52, 60, 75, 82, 90, 95, 100]

top_grades = grades[-3:]

print("საუკეთესო ქულები:", top_grades)

average = (top_grades[0] + top_grades[1] + top_grades[2]) / 3

print("საშუალო ქულა:", average)


if average > 90:
    print("საშუალო 90-ზე მეტია")
else:
    print("საშუალო 90-ზე ნაკლებია ან ტოლია")



