# შექმენი პროგრამა, რომელიც დაგეხმარება ხარჯების კონტროლში.
# შექმენი ფუნქცია `calculate_budget`, რომელიც პარამეტრად მიიღებს ხარჯების სიას (რიცხვებს).
# * პროგრამამ უნდა დაითვალოს ხარჯების ჯამი.
# * თუ ჯამი აჭარბებს 500 ლარს, გამოიტანე შეტყობინება: "ხარჯები დიდია!", თუ არა — "ბიუჯეტში ეტევით".
# * გამოიყენე `for` ციკლი სიის ელემენტების შესაკრებად.


def calculate_budget(expenses):
    total = 0

    for expense in expenses:
        total += expense

    print("სულ ხარჯი:", total)

    if total >= 500:
        print("ხარჯები დიდია!")
    else:
        print("ბიუჯეტში ეტევით")


expenses_list = [120, 80, 150, 90]
calculate_budget(expenses_list)


