# მოცემული გაქვს რიცხვების სია: `numbers = [12, 7, 9, 20, 15, 33, 4]`.
# შექმენი ორი ცარიელი სია: `evens` და `odds`.
# * `for` ციკლის და `%` (ნაშთიანი გაყოფის) გამოყენებით, ლუწი რიცხვები გადაანაწილე `evens`-ში, ხოლო კენტები `odds`-ში.
# * ბოლოს დაბეჭდე ორივე სია და მათი სიგრძე (`len()`).

numbers = [12, 7, 9, 20, 15, 33, 4]

evens = []
odds = []

for number in numbers:
    if number % 2 == 0:
        evens.append(number)
    else:
        odds.append(number)

print("ლუწი რიცხვები:", evens)
print("ლუწების რაოდენობა:", len(evens))

print("კენტი რიცხვები:", odds)
print("კენტების რაოდენობა:", len(odds))



