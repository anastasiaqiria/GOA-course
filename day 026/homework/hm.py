# დაწერე პროგრამა, რომელიც მომხმარებელს შემოატანინებს ორ რიცხვს და ოპერაციას (+, -, *, /).
# შექმენი ფუნქცია `simple_calculator`, რომელიც მიიღებს სამ პარამეტრს (ორი რიცხვი და ნიშანი).
# * გაითვალისწინე მონაცემთა ტიპების კონვერტაცია (`input()` ყოველთვის სტრინგია, შენ კი რიცხვად უნდა აქციო).
# * თუ მომხმარებელი აირჩევს გაყოფას და მეორე რიცხვი არის 0, დაბეჭდე "ნულზე გაყოფა არ შეიძლება".

def simple_calculator(num1, num2, operation):

    if operation == "+":
        print("პასუხი:", num1 + num2)

    elif operation == "-":
        print("პასუხი:", num1 - num2)

    elif operation == "*":
        print("პასუხი:", num1 * num2)

    elif operation == "/":
        if num2 == 0:
            print("ნულზე გაყოფა არ შეიძლება")
        else:
            print("პასუხი:", num1 / num2)

    else:
        print("არასწორი ოპერაცია")


number1 = float(input("შეიყვანეთ პირველი რიცხვი: "))
number2 = float(input("შეიყვანეთ მეორე რიცხვი: "))
operation = input("შეიყვანეთ ოპერაცია (+, -, *, /): ")

simple_calculator(number1, number2, operation)